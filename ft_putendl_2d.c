#include "libft.h"

void	ft_putendl2d(char **str2d)
{
	if (!str2d)
	{
		ft_putendl("(null)");
		return ;
	}
	while (*str2d)
	{
		ft_putendl(*str2d);
		str2d++;
	}
}
